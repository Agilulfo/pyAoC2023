from advent.day13.first import Pattern


def test_pattern():
    pattern = Pattern(
        [
            "#...#..##.#..##..",
            "..###.#.##..#..##",
            ".....#...#####.##",
            ".....#...#####.##",
            "..###.#.##..#..##",
            "#...#..##.#..##..",
            ".##.#.###.###..##",
            ".###.#.#..#.#.#.#",
            "##...#.#.....#.#.",
            ".#...#..##.##..#.",
            "######.#..#..##.#",
            "#######.##.....#.",
            "####.##.##.....#.",
        ]
    )

    assert pattern.find_simmetry() == 3
